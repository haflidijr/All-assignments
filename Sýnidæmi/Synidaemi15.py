
def hallo():
    # def stendur fyrir define og er notad tegar vid viljum skilgreina fall
    print("Hallo!")

# her verdur svokallad fallakall, a ensku kallad function call, ta kollum vid a fallid og
# tad keyrir. Tegar fallid hefur lokid keyrslu heldur restin af forritinu afram.
hallo()
hallo()
hallo()

def summa_tveggja_talna(x, y):
    # fall ma skilgreina tannig ad tad hafi faeribreytur, her eru taer x og y
    print(x + y)
# færibreytur í haus falls eru stundum kallaðar stikar (á ensku parameters) og þær 
# færibreytur sem fallinu eru réttar þegar kallað er á það eru kallaðar raunstikar (á ensku arguments)

tala1, tala2 = 2, 3
# Svo verður fallakallið, við réttum fallinu breyturnar okkar og fallið vinnur sína vinnu
summa_tveggja_talna(tala1, tala2) 




